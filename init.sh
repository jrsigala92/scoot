#!/bin/sh
git clone https://github.com/DevSkillsHQ/frontend-boilerplate-angular.git
cp frontend-boilerplate-angular/package.json .
cp frontend-boilerplate-angular/cypress.json .
cp -r frontend-boilerplate-angular/app* .
rm -rf frontend-boilerplate-angular
npm i
git add cypress.json package* app*
git commit -m 'Init boilerplate'
